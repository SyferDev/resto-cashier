    private void populateBillTable(Order o) {
        var model = (DefaultTableModel)tblBill.getModel();
        
        for (var p : order.products) {
            model.addRow(new Object[] {
                p.getName(),
                p.getQuantity(),
                p.getTotalAmount()
            });
        }
        
        lblAmount.setText(Double.toString(o.getTotal()));
    }

